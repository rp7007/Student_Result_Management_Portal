    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String physics = jTextField2.getText();
        String maths = jTextField3.getText();
        String dbms = jTextField4.getText();
        String os = jTextField5.getText();
        String em = jTextField6.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "#Rp@112233$7007");
            
            Statement st = con.createStatement();
            
            ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,physics,maths,dbms,os,em) values('"+rollno+"','"+physics+"','"+maths+"','"+dbms+"','"+os+"','"+em+"')");
                JOptionPane.showMessageDialog(null, "Result is saved Successfully!");
                setVisible(false);
                new insertNewResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Student is not registered!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_SaveActionPerformed

